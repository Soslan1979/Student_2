package study.alishev.lesson_3;

public class PrintLesson {
    public static void main(String[] args) {
        System.out.printf("String %d\n", 10);
        System.out.printf("String %s\n", "Hi!");
        System.out.printf("String %f\n", 10.12345);
        System.out.printf("String %.3f\n", 10.12345);
        System.out.printf("String %10d\n", 10);


    }
}
